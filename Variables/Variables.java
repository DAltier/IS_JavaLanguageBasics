package Variables;

public class Variables {

  // byte - between -128 and 127 (inclusive) 
  byte b = 120;

  // short - between -32,768 and 32,767 (inclusive)
  short s = 32000;

  // int - between -2^31 and 2^31 - 1 
  int i = 100000;

  // long - between -2^63 and 2^63 - 1
  long l = 10000000L;

  // float - single-precision 32-bit IEEE 754 floating point
  float f = 25862.7f;

  // double - double-precision 64-bit IEEE 754 floating point
  double d = 2452.34534;

  // boolean - true or false
  boolean isTrue = true;

  // char - single 16-bit Unicode character
  char c = 'a';

  // array
  int[] array = new int[20];
}